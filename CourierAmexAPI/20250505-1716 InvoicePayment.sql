ALTER PROCEDURE [dbo].[BKO_INV_InsertCraditNote]
	@Id_Reconciliacion int,
	@Id_Empresa int,
	@Id_TipoClienteProveedor varchar(50),
	@CodigoClienteProveedor varchar(100),
	@COD_MONEDA int,
	@TipoCambio decimal(18,2),
	@Monto decimal(18,2),
	@Observacion varchar(500),
	@Estado varchar(50),
	@FechaSistema datetime,
	@Fecha datetime,
	@Rubro int,
	@USER_MODIFICA int,
	@N_FACTURA int,
	@AccionNotaCredito varchar(100),
	@MONTOFACTURADOLARES decimal(18,2),
	@MONTOFACTURACOLONES decimal(18,2),
	@COD_ASIENTO varchar(80),
	@AplicarFacturaElectronica int
AS

BEGIN TRANSACTION;

BEGIN TRY

IF(@TipoCambio = 0)
BEGIN

	SELECT @TipoCambio = TIPO_CAMBIO_VENTA FROM CN_TIPO_CAMBIO WHERE ID_EMPRESA = @Id_Empresa AND COD_MONEDA = '840'

END

DECLARE @VALIDARMONTO varchar(10), @MontoNuevo decimal(18,2), @COD_NOTACREDITO int

SELECT @VALIDARMONTO = 'False', @MontoNuevo = 0

IF (@Id_Reconciliacion <> 0)
BEGIN
	SELECT @COD_NOTACREDITO = COD_NOTACREDITO FROM CN_RECONCIALIACION WHERE Id_Reconciliacion = @Id_Reconciliacion
END
ELSE
BEGIN
	SELECT @COD_NOTACREDITO = ISNULL(MAX(COD_NOTACREDITO),0) + 1 FROM CN_RECONCIALIACION
END

IF(@AccionNotaCredito <> 'Aplicar')
BEGIN
	
	INSERT INTO [dbo].[CN_RECONCIALIACION]
           ([Id_Empresa]
           ,[Id_TipoClienteProveedor]
           ,[CodigoClienteProveedor]
           ,[COD_MONEDA]
           ,[TipoCambio]
           ,[Monto]
           ,[Observacion]
           ,[Estado]
           ,[FechaSistema]
           ,[Fecha]
           ,[FEC_MODIFICA]
           ,[USER_MODIFICA]
           ,[Rubro]
           ,[N_FACTURA]
		   ,[COD_NOTACREDITO]
		   ,[Aplicacion]
		   ,[COD_ASIENTO])
     VALUES
           (@Id_Empresa
           ,@Id_TipoClienteProveedor
           ,@CodigoClienteProveedor
           ,@COD_MONEDA
           ,@TipoCambio
           ,@Monto
           ,@Observacion
           ,@Estado
           ,@FechaSistema
           ,@Fecha
           ,GETDATE()
           ,@USER_MODIFICA
           ,@Rubro
           ,@N_FACTURA
		   ,@COD_NOTACREDITO
		   ,''
		   ,@COD_ASIENTO)

	SELECT @Id_Reconciliacion = MAX(Id_Reconciliacion) FROM CN_RECONCIALIACION

	IF(@AccionNotaCredito = 'CrearAplicar')
	BEGIN
		IF(@COD_MONEDA = 188)
		BEGIN
			SELECT @MontoNuevo = @Monto - @MONTOFACTURACOLONES	
			IF (@MontoNuevo > 0)
			BEGIN
				SELECT @VALIDARMONTO = 'True'
			END
			ELSE
			BEGIN
				SELECT @VALIDARMONTO = 'False'
			END
		END
		ELSE
		BEGIN
			SELECT @MontoNuevo = @Monto - @MONTOFACTURADOLARES
			IF (@MontoNuevo > 0)
			BEGIN
				SELECT @VALIDARMONTO = 'True'
			END
			ELSE
			BEGIN
				SELECT @VALIDARMONTO = 'False'
			END
		END
	END
END
ELSE
BEGIN
	UPDATE CN_RECONCIALIACION
	SET [Id_Empresa] = @Id_Empresa
		,[Id_TipoClienteProveedor] = @Id_TipoClienteProveedor
		,[CodigoClienteProveedor] = @CodigoClienteProveedor
		,[COD_MONEDA] = @COD_MONEDA
		,[TipoCambio] = @TipoCambio
		,[Monto] = @Monto
		,[Observacion] = @Observacion
		,[Estado] = @Estado
		,[FechaSistema] = @FechaSistema
		,[Fecha] = @Fecha
		,[FEC_MODIFICA] = GETDATE()
		,[USER_MODIFICA] = @USER_MODIFICA
		,[Rubro] = @Rubro
