ALTER PROCEDURE [dbo].[BKO_INV_CancelPayment] 
@ID_EMPRESA AS INT,
@ID_PAGO AS INT,
@USUARIO AS VARCHAR(50)
AS
BEGIN

BEGIN TRAN

	DECLARE @COD_ASIENTO varchar(80)
	DECLARE @FECHA AS DATE
	DECLARE @FECHA_FACTURA AS DATE
	DECLARE @CANTIDAD AS INT
	DECLARE @MONTOLOCAL AS FLOAT
	DECLARE @MONTODOLAR AS FLOAT
	SET @FECHA = GETDATE()
	DECLARE @ID_USUARIO AS INT
	DECLARE @TIPOCAMBIO AS DECIMAL(18,2)
	DECLARE @MONTOPAGADO AS FLOAT
	DECLARE @MONTOPAGADOLOCAL AS FLOAT
	DECLARE @MONTOPAGADODOLAR AS FLOAT
	DECLARE @MONEDA AS INT, @COD_CLIENTE varchar(200)


	DECLARE @ESTADO_CUSTOMCLEARED AS INT

	SELECT @ESTADO_CUSTOMCLEARED = EST_SCANNER FROM PAR_PARAMETRO

	SELECT @ID_USUARIO = ID FROM USU_USUARIO WHERE USUARIO = @USUARIO

	SELECT @COD_ASIENTO = COD_ASIENTO FROM CN_ASIENTO_PAGO 
	WHERE  ID_EMPRESA = @ID_EMPRESA AND ID_PAGO = @ID_PAGO

	SELECT TOP 1 @TIPOCAMBIO = MON_TIPO_CAMBIO, @COD_CLIENTE = COD_CLIENTE FROM CN_ASIENTO_DETALLE
	WHERE ID_EMPRESA = @ID_EMPRESA AND COD_ASIENTO = @COD_ASIENTO

	UPDATE FC_PAGO
	SET ESTADO = 1 -- ANULADO
	WHERE ID_EMPRESA = @ID_EMPRESA AND ID = @ID_PAGO

	UPDATE FC_CAJA_MOVIMIENTOS
	SET ESTADO = 1 -- ANULADO
	WHERE ID_EMPRESA = @ID_EMPRESA AND PAGO_ID = @ID_PAGO

	SELECT @CANTIDAD = COUNT(1) 
	FROM FC_PAGOFACTURAS 
	WHERE IDPAGO = @ID_PAGO


	IF (@CANTIDAD = 1)
	BEGIN
	--print 'entro'
		
		SELECT @MONTOLOCAL = TOTALLOCAL, @MONTODOLAR = TOTAL, @MONTOPAGADO = PAGADO, @MONEDA = COD_MONEDA 
		FROM FC_PAGO WHERE ID = @ID_PAGO
		
		SELECT @FECHA_FACTURA = FECHA, @TIPOCAMBIO = TIPO_CAMBIO_VENTA FROM FC_FACTURA WHERE N_FACTURA IN (SELECT N_FACTURA FROM FC_PAGOFACTURAS WHERE IDPAGO = @ID_PAGO)


		IF(@MONEDA = 188)
		BEGIN
			SET @MONTOPAGADOLOCAL = @MONTOPAGADO
			SET @MONTOPAGADODOLAR = Convert(decimal(18,2), @MONTOPAGADO / @TIPOCAMBIO)

		END
		ELSE
		BEGIN
			SET @MONTOPAGADODOLAR = @MONTOPAGADO
			SET @MONTOPAGADOLOCAL = Convert(decimal(18,2), @MONTOPAGADO * @TIPOCAMBIO)

		END
		
		PRINT @MONTOPAGADODOLAR
		PRINT @MONTOPAGADOLOCAL

		UPDATE FC_FACTURA
		SET ESTADO = 0, -- PENDIENTE DE PAGAR
		SALDO = SALDO + @MONTOPAGADODOLAR, SALDOLOCAL = SALDOLOCAL + @MONTOPAGADOLOCAL--, PAGADO = TOTAL-SALDO--, PAGADO = PAGADO - @MONTOPAGADODOLAR -- REGRESO EL SALDO DE LA FACTURA
		WHERE N_FACTURA IN
		(
			SELECT N_FACTURA FROM FC_PAGOFACTURAS
			WHERE ID_EMPRESA = @ID_EMPRESA AND IDPAGO = @ID_PAGO  
		)

		UPDATE FC_FACTURA
		SET PAGADO = TOTAL-SALDO -- REGRESO EL SALDO DE LA FACTURA
		WHERE N_FACTURA IN
		(
			SELECT N_FACTURA FROM FC_PAGOFACTURAS
			WHERE ID_EMPRESA = @ID_EMPRESA AND IDPAGO = @ID_PAGO  
		)

	END
	ELSE
	BEGIN
		UPDATE FC_FACTURA
		SET ESTADO = 0, -- PENDIENTE DE PAGAR
		SALDO = TOTAL, SALDOLOCAL = TOTALLOCAL, PAGADO = 0 -- REGRESO EL SALDO DE LA FACTURA
		
		WHERE N_FACTURA IN
		(
			SELECT N_FACTURA FROM FC_PAGOFACTURAS
			WHERE ID_EMPRESA = @ID_EMPRESA AND IDPAGO = @ID_PAGO  
		)
	END

	-- Se regresa el paquete a custom cleared
	UPDATE PAQ_PAQUETE
	SET EST_ID = @ESTADO_CUSTOMCLEARED 
	WHERE NUMERO IN
	(
		SELECT NUMERO FROM FC_FACTURAPAQUETES 
		WHERE N_FACTURA IN
		(		
			SELECT N_FACTURA FROM FC_PAGOFACTURAS
			WHERE ID_EMPRESA = @ID_EMPRESA AND IDPAGO = @ID_PAGO  
		)
	)


IF(@ID_EMPRESA = 2)
BEGIN
	EXECUTE BKO_INV_CancelAccounting @COD_ASIENTO, @ID_EMPRESA, @FECHA, 'Anu', @ID_USUARIO, @FECHA
END

COMMIT TRAN

END

