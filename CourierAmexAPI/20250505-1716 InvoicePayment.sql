ALTER PROCEDURE [dbo].[BKO_GetPaymentInfo]
    @CompanyId INT,
    @PaymentId INT
AS
BEGIN
    SET NOCOUNT ON;
		SELECT 
		P.ID, 
		P.FECHA AS PaymentDate, 
		P.USUARIO AS [User], 
		P.PAGADO AS Paid, 
		F.N_FACTURA AS InvoiceNumber, 
		F.FECHA AS InvoiceDate, 
		F.TOTAL AS Total, 
		P.COD_MONEDA,
		CASE P.COD_MONEDA
			WHEN 840 THEN 'DOLLAR'
			WHEN 188 THEN 'COLONES'
			ELSE 'UNKNOWN'
		END AS Currency,
		F.TOTALLOCAL AS LocalTotal, 
		F.SALDO AS Balance, 
		F.SALDOLOCAL AS LocalBalance,
		EF.DESCRIPCION AS Status,
		P.PAGADO_COMISIONBANCARIA AS PaidBankCommission,
		P.PAGADO_IMPUESTOVENTAS AS PaidSalesTax,
		P.PAGADO_ADELANTORENTA AS PaidAdvanceRental,
		P.COMISIONBANCARIA AS BankCommission,
		P.IMPUESTOVENTAS AS SalesTax,
		P.ADELANTORENTA AS AdvanceRental,
		P.TIPO_CAMBIO_VENTA AS ExchangeRate
	FROM FC_PAGO P
	INNER JOIN FC_PAGOFACTURAS PF
		ON P.ID_EMPRESA = PF.ID_EMPRESA AND P.ID = PF.IDPAGO
	INNER JOIN FC_FACTURA F
		ON PF.ID_EMPRESA = F.ID_EMPRESA AND PF.N_FACTURA = F.N_FACTURA
	INNER JOIN FC_ESTADOFACTURA EF 
		ON EF.ID = P.ESTADO
	WHERE P.ID_EMPRESA = @CompanyId AND P.ID = @PaymentId

END

