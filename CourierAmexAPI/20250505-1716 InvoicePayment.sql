Create PROCEDURE [dbo].[BKO_INV_CancelAccounting]
@COD_ASIENTO VARCHAR(80),
@ID_EMPRESA int,
@FEC_PERIODO date,
@ESTADO VARCHAR(5),
@USER_MODIFICA INT,
@FEC_MODIFICA Date
AS
BEGIN
	
	BEGIN TRAN
	
		IF (SELECT IND_ESTADO FROM CN_ASIENTO WHERE COD_ASIENTO = @COD_ASIENTO AND ID_EMPRESA = @ID_EMPRESA) = 'Apli'
		BEGIN
		
			DECLARE @NUM_CUENTA varchar(50), 
				@IND_DEBCRE varchar(5),
				@MONTO_MOV float,
				@NUM_CTA_MADRE varchar(50)

			SELECT NUM_CUENTA, IND_DEBCRE, SUM(MONTO_MOV) AS MONTO_MOV, 0 AS BANDERA
			INTO #CN_ASIENTO_DETALLE_TEMP
			FROM CN_ASIENTO_DETALLE 
			WHERE COD_ASIENTO = @COD_ASIENTO
			AND ID_EMPRESA = @ID_EMPRESA
			GROUP BY NUM_CUENTA, IND_DEBCRE

			WHILE(SELECT COUNT(1) FROM #CN_ASIENTO_DETALLE_TEMP WHERE BANDERA = 0) > 0
			BEGIN

				SELECT TOP 1
				@NUM_CUENTA = NUM_CUENTA,
				@MONTO_MOV = MONTO_MOV,
				@IND_DEBCRE = IND_DEBCRE
				FROM #CN_ASIENTO_DETALLE_TEMP
				WHERE BANDERA = 0

				UPDATE #CN_ASIENTO_DETALLE_TEMP
				SET BANDERA = 1
				WHERE NUM_CUENTA = @NUM_CUENTA
				AND IND_DEBCRE = @IND_DEBCRE
				AND BANDERA = 0

				SELECT @NUM_CTA_MADRE = NUM_CTA_MADRE FROM CN_CUENTA
				WHERE ID_EMPRESA = @ID_EMPRESA AND NUM_CUENTA = @NUM_CUENTA
		
				IF (@IND_DEBCRE = 'D') 
					BEGIN
					UPDATE CN_CUENTA 
					SET MONTO_DEBER = MONTO_DEBER - @MONTO_MOV
					WHERE ID_EMPRESA = @ID_EMPRESA AND NUM_CUENTA = @NUM_CUENTA
				END
				ELSE
				BEGIN
					UPDATE CN_CUENTA 
					SET MONTO_HABER = MONTO_HABER - @MONTO_MOV
					WHERE ID_EMPRESA = @ID_EMPRESA AND NUM_CUENTA = @NUM_CUENTA
				END

				WHILE (@NUM_CTA_MADRE) <> 'No' 
				BEGIN

					IF (@IND_DEBCRE = 'D') 
					BEGIN
						UPDATE CN_CUENTA 
						SET MONTO_DEBER = MONTO_DEBER - @MONTO_MOV
						WHERE ID_EMPRESA = @ID_EMPRESA AND NUM_CUENTA = @NUM_CTA_MADRE
					END
					ELSE
					BEGIN
						UPDATE CN_CUENTA 
						SET MONTO_HABER = MONTO_HABER - @MONTO_MOV
						WHERE ID_EMPRESA = @ID_EMPRESA AND NUM_CUENTA = @NUM_CTA_MADRE
					END

					SELECT @NUM_CTA_MADRE = NUM_CTA_MADRE FROM CN_CUENTA
					WHERE ID_EMPRESA = @ID_EMPRESA AND NUM_CUENTA = @NUM_CTA_MADRE

				END

			END
	
		END

		UPDATE CN_ASIENTO SET IND_ESTADO = @ESTADO, USER_MODIFICA = @USER_MODIFICA, FEC_MODIFICA = @FEC_MODIFICA
		WHERE COD_ASIENTO = @COD_ASIENTO AND ID_EMPRESA = @ID_EMPRESA

		IF(SELECT COUNT(1) FROM CN_ASIENTO_PROVEEDOR_PAGO WHERE COD_ASIENTO_PAGO = @COD_ASIENTO) > 0
		BEGIN
			IF(SELECT COUNT(1) FROM CN_ASIENTO_PROVEEDOR WHERE COD_ASIENTO = @COD_ASIENTO) > 0
			BEGIN
		
				UPDATE CN_ASIENTO_PROVEEDOR
				SET ESTADO = 'Anulado'
				WHERE COD_ASIENTO IN (SELECT COD_ASIENTO_FACTURA FROM CN_ASIENTO_PROVEEDOR_PAGO WHERE COD_ASIENTO_PAGO = @COD_ASIENTO)			
		
			END
			ELSE
			BEGIN

				DECLARE @COD_ASIENTO_PAGO varchar(80), @COD_ASIENTO_FACTURA varchar(80), @SALDO_ANTERIOR decimal(18,2), @SALDO_ACTUAL decimal(18,2),@COD_ASIENTO_PAGO_AGREGAR_SALDO decimal(18,2)

				SELECT  @COD_ASIENTO_PAGO = @COD_ASIENTO

				UPDATE CN_ASIENTO_PROVEEDOR_PAGO 
				SET ESTADO_PAGO = 'Anulado'
				WHERE COD_ASIENTO_PAGO = @COD_ASIENTO_PAGO

				UPDATE CN_ASIENTO_PROVEEDOR
				SET ESTADO = 'Pendiente'
				WHERE COD_ASIENTO IN (SELECT COD_ASIENTO_FACTURA FROM CN_ASIENTO_PROVEEDOR_PAGO WHERE COD_ASIENTO_PAGO = @COD_ASIENTO_PAGO)

				UPDATE CN_ASIENTO_PROVEEDOR_PAGO
				SET ESTADO_PAGO = 'Pendiente'
				WHERE COD_ASIENTO_FACTURA IN (SELECT COD_ASIENTO_FACTURA FROM CN_ASIENTO_PROVEEDOR_PAGO WHERE COD_ASIENTO_PAGO = @COD_ASIENTO_PAGO)
				AND ESTADO_PAGO != 'Anulado'

				SELECT *,'P' AS BANDERA
				INTO #PROVEEDOR_PAGO
				FROM CN_ASIENTO_PROVEEDOR_PAGO 
				WHERE COD_ASIENTO_PAGO = @COD_ASIENTO_PAGO

				WHILE (SELECT COUNT(1) FROM #PROVEEDOR_PAGO WHERE BANDERA = 'P') < 0
				BEGIN

					SELECT TOP 1 @COD_ASIENTO_FACTURA = COD_ASIENTO_FACTURA, @SALDO_ANTERIOR = SALDO_ANTERIOR - SALDO_ACTUAL 
					FROM #PROVEEDOR_PAGO 
					WHERE COD_ASIENTO_PAGO = @COD_ASIENTO_PAGO AND BANDERA = 'P'

					SELECT TOP 1 @SALDO_ACTUAL = SALDO_ACTUAL,@COD_ASIENTO_PAGO_AGREGAR_SALDO = COD_ASIENTO_PAGO  
					FROM CN_ASIENTO_PROVEEDOR_PAGO WHERE COD_ASIENTO_FACTURA = @COD_ASIENTO_FACTURA AND COD_ASIENTO_PAGO != @COD_ASIENTO_PAGO AND ESTADO_PAGO != 'Anulado'
					ORDER BY CONVERT(int,COD_ASIENTO_PAGO) DESC

					UPDATE CN_ASIENTO_PROVEEDOR_PAGO
					SET ESTADO_PAGO = 'Pendiente', SALDO_ACTUAL = SALDO_ACTUAL + @SALDO_ANTERIOR
					WHERE COD_ASIENTO_PAGO = @COD_ASIENTO_PAGO_AGREGAR_SALDO

					UPDATE #PROVEEDOR_PAGO 
					SET BANDERA = 'A'
					WHERE BANDERA = 'P' AND COD_ASIENTO_FACTURA = @COD_ASIENTO_FACTURA 
					AND COD_ASIENTO_PAGO = @COD_ASIENTO_PAGO 
					AND ESTADO_PAGO != 'Anulado'

				END

			END
		END

		DECLARE @MAXCOD_ASIENTO int

		SELECT @MAXCOD_ASIENTO = MAX(CONVERT(int,COD_ASIENTO)) + 1 FROM CN_ASIENTO
		
		INSERT INTO CN_ASIENTO
		SELECT [ID_EMPRESA] ,@MAXCOD_ASIENTO,[FEC_PERIODO], [FEC_ASIENTO],
		CONVERT(varchar(200),'Reversado- ' + [DET_ASIENTO]),'Apli',[COD_SISTEMA_ORIGEN],[IND_SISTEMA],CONVERT(date,GETDATE()),[USER_INCLUSION],[FEC_MODIFICA],[USER_MODIFICA],[CUADRADO],'A'
		FROM CN_ASIENTO WHERE COD_ASIENTO = @COD_ASIENTO

		INSERT INTO CN_ASIENTO_DETALLE
		SELECT [ID_EMPRESA],@MAXCOD_ASIENTO,[FEC_PERIODO],
		[NUM_LINEA_ASIENTO],[MONTO_MOV],[NUM_CUENTA],CASE WHEN [IND_DEBCRE] = 'D' THEN 'C' ELSE 'D' END,[MON_TIPO_CAMBIO],[MONTO_ORIGINAL],[MONEDA_ORIGINAL],[COD_CLIENTE]
		FROM CN_ASIENTO_DETALLE WHERE COD_ASIENTO = @COD_ASIENTO

		INSERT INTO [dbo].[CN_ASIENTO_REVERSADO]
			   ([COD_ASIENTO]
			   ,[COD_ASIENTO_REVERSADO])
		 VALUES
			   (@MAXCOD_ASIENTO
			   ,@COD_ASIENTO)
	
	COMMIT TRAN

END


